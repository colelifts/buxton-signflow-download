import type { BeatEvent, LevelData, LevelObject, MissionRank, PlayerForm, SkinDefinition } from "../game/types";

let serial = 0;
const id = (prefix: string) => `${prefix}-${serial++}`;

const block = (x: number, y: number, w: number, h = 42, variant = "concrete"): LevelObject => ({
  id: id("block"),
  type: "block",
  x,
  y,
  w,
  h,
  variant,
});

const spike = (x: number, y = 512, w = 42, h = 48): LevelObject => ({
  id: id("spike"),
  type: "spike",
  x,
  y,
  w,
  h,
});

const saw = (x: number, y: number, r = 30): LevelObject => ({
  id: id("saw"),
  type: "saw",
  x,
  y,
  r,
});

const flame = (x: number, y = 500, w = 52, h = 64): LevelObject => ({
  id: id("flame"),
  type: "flame",
  x,
  y,
  w,
  h,
});

const pad = (x: number, y = 525): LevelObject => ({
  id: id("pad"),
  type: "pad",
  x,
  y,
  w: 58,
  h: 18,
  color: "#2ce7ff",
});

const orb = (x: number, y: number, color = "#a855f7"): LevelObject => ({
  id: id("orb"),
  type: "orb",
  x,
  y,
  r: 24,
  color,
});

const seal = (x: number, y: number): LevelObject => ({
  id: id("seal"),
  type: "seal",
  x,
  y,
  r: 18,
});

const portal = (x: number, y: number, targetForm: PlayerForm, trigger?: "domainSurge" | "drop"): LevelObject => ({
  id: id("portal"),
  type: "portal",
  x,
  y,
  w: 68,
  h: 140,
  targetForm,
  trigger,
});

const gate = (x: number, y = 400): LevelObject => ({
  id: id("gate"),
  type: "gate",
  x,
  y,
  w: 110,
  h: 160,
  trigger: "domainSurge",
});

const laser = (x: number, y: number, w: number, h: number, beat: number): LevelObject => ({
  id: id("laser"),
  type: "laser",
  x,
  y,
  w,
  h,
  beat,
});

const decor = (x: number, y: number, variant: string, w = 120, h = 120): LevelObject => ({
  id: id("decor"),
  type: "decor",
  x,
  y,
  w,
  h,
  variant,
});

const boss = (x: number, y: number, variant = "shadow-oracle"): LevelObject => ({
  id: id("boss"),
  type: "boss",
  x,
  y,
  w: 320,
  h: 300,
  variant,
});

const checkpoint = (x: number, y = 490): LevelObject => ({
  id: id("checkpoint"),
  type: "checkpoint",
  x,
  y,
  w: 44,
  h: 70,
  trigger: "checkpoint",
});

const beats = (count: number, strongEvery = 8): BeatEvent[] =>
  Array.from({ length: count }, (_, beat) => ({
    beat,
    type: beat % 32 === 0 ? "surge" : beat % strongEvery === 0 ? "shake" : "pulse",
    strength: beat % 32 === 0 ? 1.25 : beat % strongEvery === 0 ? 0.72 : 0.34,
  }));

const palette = (background: string, primary = "#a855f7", secondary = "#2ce7ff", danger = "#ff335f") => ({
  primary,
  secondary,
  danger,
  spirit: "#f8fbff",
  background,
});

const baseLevel = (
  idName: string,
  name: string,
  missionRank: MissionRank,
  bpm: number,
  backgroundTheme: string,
  speed: number,
  length: number,
): Omit<LevelData, "objects" | "hazards" | "portals" | "coins" | "decorations" | "beatEvents" | "colorPalette"> => ({
  id: idName,
  name,
  missionRank,
  music: `${idName}-synth`,
  bpm,
  backgroundTheme,
  kind: "classic",
  playerStart: { x: 120, y: 490 },
  speed,
  length,
});

